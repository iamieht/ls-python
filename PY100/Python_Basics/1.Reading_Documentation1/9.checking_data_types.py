# Referring to the official Python documentation, how would you identify the data type of the following values?
# 23.5
# 'Call me Ishmael.'
# False
# 0
# None
# https://docs.python.org/3/library/functions.html#type

print(type(23.5))
print(type('Call me Ishmael.'))
print(type(False))
print(type(0))
print(type(None))
